/**
 * Convert a string to a 2D array of booleans. Rows are separated by newlines.
 *     '-'s represent dead cells and '@'s represent live ones.
 * Convenience method because examples are much easier to visualize like this.
 * @param {string} array_string The string to convert. All characters must be
 *     '-', '@', or '\n'.
 * @throws {TypeError} If any of the values representing cells are not '-' or
 *     '@'.
 * @return {Grid} A parallel array where all '-'s and '@'s have been converted
 *     to booleans.
 */
function string_to_boolean(array_string) {
    var array = array_string.split('\n');
    var converted = [];
    for (var i = 0; i < array.length; i++) {
        converted.push([]);
        for (var j = 0; j < array[0].length; j++) {
            if (array[i][j] === '@') {
                converted[i].push(true);
            } else if (array[i][j] === '-') {
                converted[i].push(false);
            } else {
                throw new TypeError('Unexpected array value ' + array[i][j] +
                    ' at ' + 'array[' + i + '][' + j +'].');
            }
        }
    }
    return converted;
}

